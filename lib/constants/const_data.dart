        throw Exception("Invalid test name");
    }

    if (age > 14) {
      age = 14;
    } else if (age < 9) {
      age = 9;
    }

    // Find the correct pointsTable entry based on age and gender
    Map<String, dynamic>? tableEntry = pointsTable.firstWhere(
      (entry) => entry['age'] == age && entry['gender'] == gender,
