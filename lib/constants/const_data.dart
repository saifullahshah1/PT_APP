  // Helper function to parse the reps range string and return a numerical value
  int _parseRepsRange(String rangeString) {
    if (rangeString.startsWith('> ')) {
      return int.parse(rangeString.substring(2));
    } else if (rangeString.startsWith('< ')) {
      return int.parse(rangeString.substring(2)) - 1;
    } else {
      List<String> parts = rangeString.split(' - ');
      return (int.parse(parts[0]) + int.parse(parts[1])) ~/ 2;
    }
  }

  // Helper function to parse the time range string and return a numerical value
  double _parseTimeRange(String rangeString) {
    rangeString = rangeString.replaceAll('<', '').replaceAll('>', '');
    List<String> parts = rangeString.split(' - ');
    if (parts.length == 1) {
      return double.parse(parts[0]);
    } else {
      return (double.parse(parts[0]) + double.parse(parts[1])) / 2.0;
    }
  }

